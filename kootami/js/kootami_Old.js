var calcTara = function(myStar,myFactor,myScheme){

	let temp = 0;
	
	if(myScheme == 28){

		// add abhijit if scheme is 28.
		aStarNames27.splice(21, 0, "అభిజిత్");
		
		temp = myStar + myFactor;
		if ( temp > 27 )
		{
			temp = temp - 28;
		}
		
		return temp;
		
	}else{

		
		temp = myStar + myFactor;

		if ( temp > 26 )
		{
			temp = temp - 27;
		}
		return temp;
	}

}


